import java.util.*;
public class CloudberryJam{
    public static void main(String[] args) {
        Scanner sc=new Scanner(System.in);
        int n=sc.nextInt();
        for (int i=0;i<n;i++){
            int value=sc.nextInt();
            int berries=(value*2);
            System.out.println(berries);
        }
    }
}
